import UIKit

extension UIFont {
    
    var main : UIFont {
        return UIFont.systemFont(ofSize: 16, weight: .heavy)
    }
    
    var SCDream1_14 : UIFont {
        return UIFont.init(name: "S-CoreDream-1Thin", size: 14)!
    }
    
    var SCDream5_14 : UIFont {
        return UIFont.init(name: "S-CoreDream-5Medium", size: 14)!
    }
    var SCDream8_14 : UIFont {
        return UIFont.init(name: "S-CoreDream-8Heavy", size: 14)!
    }
    
    
    
    
}

